package com.lsj.option.framework.base;

import android.os.Bundle;
import android.support.annotation.NonNull;
import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;

import com.lsj.option.framework.mvp.IPresenter;
import com.lsj.option.framework.mvp.IView;

/**
 * <pre>
 *     author : jacks
 *     e-mail : 363525749@qq.com
 *     time   : 2019/12/03
 *     version: 1.0
 *     desc   : BaseFragment
 * </pre>
 * <p>
 * 抽象创建Presenter方法。数据懒加载。
 */
public abstract class BaseFragment<P extends IPresenter> extends Fragment implements IView, IFragment {
    protected boolean isInit = false;
    protected boolean isLoad = false;
    protected P mPresenter;

    protected abstract P createPresenter();

    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        isInit = true;
        if (mPresenter == null && createPresenter() != null) {
            mPresenter = createPresenter();
            mPresenter.attachView(this);
        }
        View viewCreated = onCreateMyView(inflater,container,savedInstanceState);
        if (viewCreated != null) {
            return viewCreated;
        }
        return super.onCreateView(inflater, container, savedInstanceState);
    }

    protected abstract View onCreateMyView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState);

    @Override
    public void onDestroyView() {
        super.onDestroyView();
        if (mPresenter != null) {
            mPresenter.detachView();
            mPresenter = null;
        }
        hideLoading();
    }

    @Override
    public void onStart() {
        super.onStart();
        isCanLoadData();
    }

    @Override
    public void setUserVisibleHint(boolean isVisibleToUser) {
        super.setUserVisibleHint(isVisibleToUser);
        isCanLoadData();
    }

    /**
     * 懒加载
     * 是否可以加载数据
     * 可以加载数据的条件：
     * 1.视图已经初始化
     * 2.视图对用户可见
     */
    private void isCanLoadData() {
        if (!isInit) return;
        if (getUserVisibleHint()) {
            if (!isLoad) {
                lazyLoad();
            }
            isLoad = true;
        } else {
            if (isLoad) {
                stopLoad();
            }
        }
    }

    /**
     * 当视图初始化并且对用户可见的时候去真正的加载数据
     */
    protected void lazyLoad() {
    }

    /**
     * 当视图已经对用户不可见并且加载过数据，如果需要在切换到其他页面时停止加载数据，可以调用此方法
     */
    protected void stopLoad() {
    }

}
